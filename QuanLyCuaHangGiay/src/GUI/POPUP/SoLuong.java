    public static void getSoLuongSanPhamBan(){
        String SoLuong = txtSoLuongSanPhamBan.getText();
        if(!So(SoLuong)){
            GUI.Sweet.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Lỗi!","Số lượng không hợp lệ",1);
            SoLuongMuaHopLe = -1;
        }
        else {
            long SoLuongBan = Long.parseLong(SoLuong);
            SanPhamDTO SanPham = quanlycuahanggiay.quanlycuahanggiay.SanPhamBUS.getByMaSanPham(currentChiTietDonHang.getMaSanPham());
            //System.out.println("SOLUONG: "+SanPham.getSoLuong());
            if(SoLuongBan > Long.parseLong(SanPham.getSoLuong())){
                GUI.Sweet.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Lỗi!","Số lượng mua vượt quá số lượng đang có",1);
                SoLuongMuaHopLe = -1;
            }
            else {
                SoLuongMuaHopLe = SoLuongBan;
                //return SoLuongMuaHopLe;
            }
        } 
    }
