    public void Them(){
        long ThanhTien = 0;
        getSoLuongSanPhamBan();
        if(SoLuongMuaHopLe != -1 && SoLuongMuaHopLe != 0){
            if(GioHang.size()==0){
                Them1SanPham();
                /*
                ((javax.swing.JFrame) SwingUtilities.getWindowAncestor(quanlycuahanggiay.quanlycuahanggiay.bg)).setEnabled(true);
                currentChiTietDonHang.setSoLuong(SoLuongMuaHopLe+"");
                ThanhTien = SoLuongMuaHopLe*Integer.parseInt(GUI.POPUP.ThemSanPhamVaoDonHangJFrame.SanPham.getDonGia());
                currentChiTietDonHang.setThanhTien(Integer.toString(ThanhTien));
                GioHang.add(currentChiTietDonHang);
                ThemSanPhamVaoDonHangJFrame.fetchWhenAddingGioHang();
                fetchAllBottom();
                this.dispose();
                */
            }
            else{
                int flag = 1;
                for(int i=0;i<GioHang.size();i++){
                    if(GioHang.get(i).getMaSanPham().equals(SanPham.getMaSanPham())){
                        System.out.println("SP da co");
                        SoLuongMuaHopLe = Long.parseLong(GioHang.get(i).getSoLuong())+SoLuongMuaHopLe;
                        if(SoLuongMuaHopLe>Long.parseLong(SanPham.getSoLuong())){
                            GUI.Sweet.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Lỗi!","Tổng số lượng mua vượt quá số lượng hiện có",1);
                            return;
                        }
                        else{
                            long DonGia = Long.parseLong(SanPham.getDonGia());
                            GioHang.get(i).setSoLuong(SoLuongMuaHopLe+"");
                            System.out.println(SoLuongMuaHopLe);
                            System.out.println(DonGia);
                            GioHang.get(i).setThanhTien((tinhThanhTien(SoLuongMuaHopLe,DonGia)+""));
                            ThemSanPhamVaoDonHangJFrame.fetchWhenAddingGioHang();
                            fetchAllBottom();
                            flag = 0;
                            ((javax.swing.JFrame) SwingUtilities.getWindowAncestor(GUI.THEMMOI.TaoDonHangJPanel.TSPVDHJF.getRootPane())).setEnabled(true);
                            this.dispose();
      
                        }
                    }
                }
                if(flag == 1){
                    Them1SanPham();
                    /*
                    System.out.println("SP chua co");
                    ((javax.swing.JFrame) SwingUtilities.getWindowAncestor(quanlycuahanggiay.quanlycuahanggiay.bg)).setEnabled(true);
                    int DonGia = Integer.parseInt(SanPham.getDonGia());
                    currentChiTietDonHang.setSoLuong(SoLuongMuaHopLe+"");
                    currentChiTietDonHang.setThanhTien((tinhThanhTien(SoLuongMuaHopLe,DonGia)+""));
                    GioHang.add(currentChiTietDonHang);
                    ThemSanPhamVaoDonHangJFrame.fetchWhenAddingGioHang();
                    fetchAllBottom();
                    this.dispose();
                    */
                }
            } 
        }
        if(SoLuongMuaHopLe==0) GUI.Sweet.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Lỗi!","Số lượng mua không thể bằng 0",1);
    }
    
