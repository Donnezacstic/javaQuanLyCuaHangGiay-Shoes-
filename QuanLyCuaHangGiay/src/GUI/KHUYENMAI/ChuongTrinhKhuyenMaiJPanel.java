    private void tblChuongTrinhKhuyenMaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChuongTrinhKhuyenMaiMouseClicked
        int column = tblChuongTrinhKhuyenMai.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tblChuongTrinhKhuyenMai.getRowHeight();

        if (row < tblChuongTrinhKhuyenMai.getRowCount() && row >= 0 && column < tblChuongTrinhKhuyenMai.getColumnCount() && column >= 0) {
            this.removeAll();
            this.setLayout(new BorderLayout());
            currentChuongTrinhKhuyenMai = quanlycuahanggiay.quanlycuahanggiay.ChuongTrinhKhuyenMaiBUS.getByMaChuongTrinhKhuyenMai((String) tblChuongTrinhKhuyenMai.getModel().getValueAt(row, 0));
            this.add(new ThongTinChuongTrinhKhuyenMaiJPanel());
            this.validate();
            this.repaint();

        }
    }//GEN-LAST:event_tblChuongTrinhKhuyenMaiMouseClicked

    private void getAdvancedSearch() {
        HashMap<String, String> advancedSearch = new HashMap<>();
        advancedSearch.put("MaChuongTrinhKhuyenMai", txtTimKiemMaChuongTrinh.getText());
        advancedSearch.put("TenChuongTrinhKhuyenMai", txtTimKiemTenChuongTrinh.getText());
        arraySearch = quanlycuahanggiay.quanlycuahanggiay.ChuongTrinhKhuyenMaiBUS.advancedSearch(advancedSearch);
        search();
    }
