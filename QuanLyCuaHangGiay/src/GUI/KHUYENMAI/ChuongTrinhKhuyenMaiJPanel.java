    public static void fetchAll() {
        listTableModel.setRowCount(0);
        quanlycuahanggiay.quanlycuahanggiay.ChuongTrinhKhuyenMaiBUS.ChuongTrinhKhuyenMaiList.forEach(ChuongTrinhKhuyenMai -> {
            Vector row = new Vector();
            row.add(ChuongTrinhKhuyenMai.getMaChuongTrinhKhuyenMai());
            row.add(ChuongTrinhKhuyenMai.getTenChuongTrinh());
            row.add(TimestampToDateString(ChuongTrinhKhuyenMai.getNgayTao(), 1));
            if (quanlycuahanggiay.quanlycuahanggiay.ChiTietKhuyenMaiBUS.getSoLuongbyMaChuongTrinhKhuyenMai(ChuongTrinhKhuyenMai.getMaChuongTrinhKhuyenMai()) == null) {
                row.add(0);
            }
            row.add(quanlycuahanggiay.quanlycuahanggiay.ChiTietKhuyenMaiBUS.getSoLuongbyMaChuongTrinhKhuyenMai(ChuongTrinhKhuyenMai.getMaChuongTrinhKhuyenMai()));
            listTableModel.addRow(row);
        });
        tblChuongTrinhKhuyenMai.setModel(listTableModel);

    }

